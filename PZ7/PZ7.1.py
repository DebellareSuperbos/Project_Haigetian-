#Даны строки S и S0. Найти количество вхождений строки S0 в строку S

print("Задание 1: Количество вхождений подстроки")
try:
    S = input("Введите строку S: ")
    S0 = input("Введите подстроку S0: ")
    
    if S0 == "":
        print("Ошибка: подстрока не может быть пустой")
    else:
        count = S.count(S0)
        print(f"Количество вхождений: {count}")
        
except Exception as e:
    print(f"Ошибка: {e}")
