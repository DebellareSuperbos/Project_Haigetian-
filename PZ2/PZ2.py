#Дни недели пронумерованы следующим образом: 1 — понедельник, 2 — вторник, ..., 6 — суббота, 7 — воскресенье. Дано целое число K, лежащее в диапазоне 1-365. Определить номер дня недели для K-го дня года, если известно, что в этом году 1 января было вторником
try:
  
    k = int(input("Введите K (1-365): "))
    if k < 1 or k > 365:
        print("Ошибка: K должен быть от 1 до 365")
    else:
        day_num = (2 + (k - 1)) % 7
        if day_num == 0:
            day_num = 7
        days = {
            1: "понедельник",
            2: "вторник", 
            3: "среда",
            4: "четверг",
            5: "пятница",
            6: "суббота",
            7: "воскресенье"
        }
        print(f"{k}-й день года - это {days[day_num]} (день недели №{day_num})")

except ValueError:
    print("Ошибка: введите целое число")
