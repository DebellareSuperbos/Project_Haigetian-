# Найти сумму чисел ряда 1,2,3,...,60 с использованием функции нахождения суммы. Использовать локальные переменные.
def sum_series():
    """Функция вычисляет сумму чисел от 1 до 60"""
    total = 0  
    for i in range(1, 61):
        total += i
    return total

print("Задача 1:")
result = sum_series()
print(f"Сумма чисел от 1 до 60: {result}")
